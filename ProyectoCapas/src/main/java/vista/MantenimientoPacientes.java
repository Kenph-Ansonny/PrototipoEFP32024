    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Paciente");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Genero");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Estatus");
        PacientesDAO pacientesDAO = new PacientesDAO();
        List<Pacientes> pacientes = pacientesDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[7];
        for (int i = 0; i < pacientes.size(); i++) {
            dato[0] = pacientes.get(i).getId_paciente();
            dato[1] = pacientes.get(i).getNombre_paciente();
            dato[2] = pacientes.get(i).getApellido_paciente();
            dato[3] = pacientes.get(i).getGenero_paciente();
            dato[4] = pacientes.get(i).getDireccion_paciente();
            dato[5] = pacientes.get(i).getTelefono_paciente();
            dato[6] = pacientes.get(i).getEstatus_paciente();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

